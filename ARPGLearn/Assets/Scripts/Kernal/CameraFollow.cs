using UnityEngine;
using System.Collections;

namespace kernal
{
    public class CameraFollow : MonoBehaviour
    {
        // The target we are following
        public Transform target = null;
        // The distance in the x-z plane to the target
        public float distance = 6.0f;  
        // the height we want the camera to be above the target
        public float height = 8.0f;
        // How much we 
        public float heightDamping = 4.0f;
        public float rotationDamping = 0.0f;

        
        void Start()
        {
            target = GameObject.FindGameObjectWithTag("Player").transform;
        }

        float a = 2.0f;
        // Update is called once per frame
        void Update()
        {
            a -= Time.deltaTime;
            if (a <= 0)
            {
                target = GameObject.FindGameObjectWithTag("Player").transform;
                a = 2.0f;
            }
        }

        void LateUpdate()
        {
            if (!target)
                return;
            var wantedRotationAngle = target.eulerAngles.y; //旋转的目标角度
            var wantedHeight = target.position.y + height;//目标高度
            var currentRotationAngle = transform.eulerAngles.y; //当前角度
            var currentHeight = transform.position.y;//当前高度
            // Damp the rotation around the y-axis      插值求出当前旋转角度
            currentRotationAngle = Mathf.LerpAngle(currentRotationAngle, wantedRotationAngle, rotationDamping * Time.deltaTime);
            // Damp the height          插值求出当前高度
            currentHeight = Mathf.Lerp(currentHeight, wantedHeight, heightDamping * Time.deltaTime);

            // Convert the angle into a rotation
            Quaternion currentRotation = Quaternion.Euler(0, currentRotationAngle, 0);
            // Set the position of the camera on the x-z plane to:
            // distance meters behind the target
            transform.position = target.position;       //获取到目标的位置
            transform.position -= currentRotation * Vector3.forward * distance;//摄像机距离目标的位置
            // Set the height of the camera
            //transform.position.y = currentHeight;       
            transform.position = new Vector3(transform.position.x, currentHeight, transform.position.z);//摄像机的高度
            // Always look at the target
            transform.LookAt(target);
            // Use this for initialization
        }
    }//class_end
}